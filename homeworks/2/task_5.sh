#!/bin/bash

# Описание:
# Скрипт запускает три команды sleep с разными интервалами (115, 117 и 119 секунд)
# в фоновом режиме для демонстрации управления процессами с помощью jobs, fg и bg.

sleep 115 &
sleep 117 &
sleep 119 &

# Наблюдение:
# После запуска через команду source task_5.sh в выводе jobs отображаются три фоновых процесса.
# С помощью fg можно вернуть любую задачу на передний план.
# После приостановки (Ctrl+Z) и повторного запуска через bg задача снова переходит в фоновый режим.
# Команда jobs показывает изменения статусов (Running, Stopped, Done).

# Вывод:
# В ходе выполнения видно, что процессы, запущенные с амперсандом (&), работают в фоне.
# Команды jobs, fg и bg позволяют управлять их состоянием и наблюдать завершение задач.
